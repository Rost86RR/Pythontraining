# Arithmetic operators-------------------------------------------------------------------------
a = 5
b = 2

print(a + b) #addition of a and b
print(a - b) #substraction
print(a * b) #multiplication
print(a / b)  # 2.5
print(a // b) # 2 quotient
print(a % b)  # 1
print(a ** b) # 5 ** 2 = 25

# Relational operators--------------------------------------------------------------------------
# >, <, >=, <=, !=
# Relational operators always return boolean value true/false
a = 5
b = 10

print(a > b) # False
print(a < b) # True
print(a == b) # False
print(a != b) # True
print(a >= b) # False
print(a <= b) # True

# Logical operators-------------------------------------------------------------------------------
# and, or, not;
# Logical operators always return boolean value true/false
a = True
b = False

print(a and b) # False
print(a or b) # True
print(not a) # False